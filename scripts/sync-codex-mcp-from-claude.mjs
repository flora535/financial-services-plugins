#!/usr/bin/env node
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';

const START_MARKER = '# >>> financial-services-plugins MCP (generated) >>>';
const END_MARKER = '# <<< financial-services-plugins MCP (generated) <<<';

function usage() {
  console.error(
    [
      'Usage:',
      '  node scripts/sync-codex-mcp-from-claude.mjs [--source <path>] [--target <path>] [--dry-run]',
      '',
      'Defaults:',
      '  --source financial-analysis/.mcp.json',
      '  --target ~/.codex/config.toml',
    ].join('\n')
  );
}

function parseArgs(argv) {
  const args = {
    source: 'financial-analysis/.mcp.json',
    target: '~/.codex/config.toml',
    dryRun: false,
  };

  for (let i = 0; i < argv.length; i += 1) {
    const token = argv[i];
    if (token === '--source') {
      args.source = argv[i + 1];
      i += 1;
    } else if (token === '--target') {
      args.target = argv[i + 1];
      i += 1;
    } else if (token === '--dry-run') {
      args.dryRun = true;
    } else if (token === '--help' || token === '-h') {
      usage();
      process.exit(0);
    } else {
      console.error(`Unknown argument: ${token}`);
      usage();
      process.exit(1);
    }
  }

  if (!args.source || !args.target) {
    usage();
    process.exit(1);
  }

  return args;
}

function expandHome(p) {
  if (p === '~') return os.homedir();
  if (p.startsWith('~/')) return path.join(os.homedir(), p.slice(2));
  return p;
}

function tomlString(value) {
  return `"${String(value)
    .replace(/\\/g, '\\\\')
    .replace(/\"/g, '\\\"')
    .replace(/\n/g, '\\n')}"`;
}

function tomlArray(values) {
  return `[${values.map(tomlString).join(', ')}]`;
}

function validateServer(name, cfg) {
  const isHttp = cfg && (cfg.type === 'http' || typeof cfg.url === 'string');

  if (isHttp) {
    if (typeof cfg.url !== 'string' || cfg.url.length === 0) {
      throw new Error(`mcpServers.${name}: http server requires non-empty url`);
    }
    return 'http';
  }

  if (typeof cfg.command !== 'string' || cfg.command.length === 0) {
    throw new Error(`mcpServers.${name}: stdio server requires non-empty command`);
  }
  if (cfg.args != null && !Array.isArray(cfg.args)) {
    throw new Error(`mcpServers.${name}: args must be an array when present`);
  }
  if (cfg.env != null && (typeof cfg.env !== 'object' || Array.isArray(cfg.env))) {
    throw new Error(`mcpServers.${name}: env must be an object when present`);
  }
  return 'stdio';
}

function buildGeneratedBlock(mcpServers, sourcePath) {
  const names = Object.keys(mcpServers).sort();
  const lines = [
    START_MARKER,
    '# Auto-generated by scripts/sync-codex-mcp-from-claude.mjs',
    `# Source: ${sourcePath}`,
  ];

  for (const name of names) {
    const cfg = mcpServers[name];
    const kind = validateServer(name, cfg);

    lines.push('');
    lines.push(`[mcp_servers.${name}]`);

    if (kind === 'http') {
      lines.push(`url = ${tomlString(cfg.url)}`);
      if (typeof cfg.bearer_token_env_var === 'string' && cfg.bearer_token_env_var.length > 0) {
        lines.push(`bearer_token_env_var = ${tomlString(cfg.bearer_token_env_var)}`);
      }
      continue;
    }

    lines.push(`command = ${tomlString(cfg.command)}`);
    lines.push(`args = ${tomlArray((cfg.args || []).map(String))}`);

    if (cfg.env && Object.keys(cfg.env).length > 0) {
      lines.push('');
      lines.push(`[mcp_servers.${name}.env]`);
      for (const envKey of Object.keys(cfg.env).sort()) {
        lines.push(`${envKey} = ${tomlString(cfg.env[envKey])}`);
      }
    }
  }

  lines.push('');
  lines.push(END_MARKER);
  lines.push('');
  return lines.join('\n');
}

function upsertBlock(existingToml, block) {
  const start = existingToml.indexOf(START_MARKER);
  const end = existingToml.indexOf(END_MARKER);

  if ((start >= 0 && end < 0) || (start < 0 && end >= 0)) {
    throw new Error('Config contains only one MCP marker; fix markers before re-running.');
  }

  if (start >= 0 && end > start) {
    const afterEnd = end + END_MARKER.length;
    const before = existingToml.slice(0, start).replace(/[\s\n]*$/, '\n\n');
    const after = existingToml.slice(afterEnd).replace(/^\s*\n*/, '\n\n');
    return `${before}${block}${after}`.replace(/\n{3,}/g, '\n\n').trimEnd() + '\n';
  }

  const base = existingToml.trimEnd();
  if (base.length === 0) return block;
  return `${base}\n\n${block}`;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const sourcePath = path.resolve(expandHome(args.source));
  const targetPath = path.resolve(expandHome(args.target));

  if (!fs.existsSync(sourcePath)) {
    throw new Error(`Source file not found: ${sourcePath}`);
  }

  const raw = fs.readFileSync(sourcePath, 'utf8');
  const parsed = JSON.parse(raw);

  if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
    throw new Error('Source JSON must be an object');
  }
  if (!parsed.mcpServers || typeof parsed.mcpServers !== 'object' || Array.isArray(parsed.mcpServers)) {
    throw new Error('Source JSON must contain object: mcpServers');
  }

  const block = buildGeneratedBlock(parsed.mcpServers, sourcePath);

  const targetDir = path.dirname(targetPath);
  if (!args.dryRun) {
    fs.mkdirSync(targetDir, { recursive: true });
  }

  const existing = fs.existsSync(targetPath) ? fs.readFileSync(targetPath, 'utf8') : '';
  const merged = upsertBlock(existing, block);

  if (args.dryRun) {
    process.stdout.write(merged);
    return;
  }

  fs.writeFileSync(targetPath, merged, 'utf8');
  console.log(`Updated financial MCP block in: ${targetPath}`);
}

try {
  main();
} catch (error) {
  console.error(`sync-codex-mcp-from-claude failed: ${error.message}`);
  process.exit(1);
}
